def test_dags_integrity(dagbag):
    assert dagbag.import_errors == {}, f"Import errors found: {dagbag.import_errors}"
    print("===============")
    print(dagbag.import_errors)

    expected_dags_id = ["update_db", "data_quality"]
    loaded_dags_id = list(dagbag.dags.keys())
    print("===============")
    print(dagbag.dags.keys())

    for dag_id in expected_dags_id:
        assert dag_id in loaded_dags_id, f"DAG {dag_id} is missing"

    assert dagbag.size() == 2
    print("===============")
    print(dagbag.size())

    expected_tasks_count = {
        "produce_json": 5,
        "update_db": 3,
        "data_quality": 2,
    }
    print("===============")

    for dag_id, dag in dagbag.dags.items():
        expected_count = expected_tasks_count[dag_id]
        actual_count = len(dag.tasks)
        assert (
            expected_count == actual_count
        ), f"DAG {dag_id} has {actual_count} tasks, expected {expected_count}"
        print("===============")
        print(dag_id, len(dag.tasks))